  function mergePacks() {
    if (typeof I18N === "undefined") return;
    Object.keys(packs).forEach((lang) => {
      I18N[lang] = Object.assign({}, baseEn, I18N[lang] || {}, packs[lang] || {}, profilePaymentPacks[lang] || {});
    });
  }

  function currentLang() {
    if (typeof state !== "undefined" && state.lang) return state.lang;
    return document.getElementById("languageSelect")?.value || "uk";
  }

  function applyDirection() {
    const lang = currentLang();
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    document.documentElement.lang = lang;
  }

  function applyCompleteLanguagePack() {
    mergePacks();
    applyDirection();
    if (typeof applyI18nToDom === "function") applyI18nToDom();
    if (typeof renderAll === "function") renderAll();
  }

  mergePacks();
  window.applyCompleteLanguagePack = applyCompleteLanguagePack;

  document.addEventListener("DOMContentLoaded", () => {
    applyCompleteLanguagePack();
    document.getElementById("languageSelect")?.addEventListener("change", () => {
      setTimeout(applyCompleteLanguagePack, 0);
    });
  });
